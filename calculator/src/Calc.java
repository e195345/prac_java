//演算処理について記入するクラス

public class Calc {
    public static int tasu(int a, int b) {
        return(a+b);
    }

    public static int hiku(int a, int b) {
        return(a-b);
    }

    public static int kake(int a, int b) {
        return(a*b);
    }

    public static int waru(int a, int b) {
        return(a/b);
    }
}